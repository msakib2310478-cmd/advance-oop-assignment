package com.fastinglog.model;

/**
 * Enumeration representing the type of fasting.
 * Encapsulates the different fasting categories supported by the application.
 */
public enum FastType {
    /**
     * Religious fasting (e.g., Ramadan, Lent, etc.)
     */
    RELIGIOUS,
    
    /**
     * Intermittent fasting for health purposes
     */
    INTERMITTENT
}
